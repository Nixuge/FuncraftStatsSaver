def grab_page_count(url: str) -> int:
    req = httpx.Client().get(url)
    if req.status_code != 200:
        print(f"fail, retrying ({req.status_code})")
        time.sleep(1)
        return grab_page_count(url)

    soup = BeautifulSoup(req.text, "html.parser")
    page_browser = soup.find("span", {"class": "pageNavHeader"})
    if not page_browser:
        return 1
    
    end = page_browser.text.replace("Page ", "").split(" sur ")[1]
    return int(end)


def gen():
    NEW_VALID_URLS = {}
    for url in THREADS_PAGES_NODUPLICATE:
        key = url.replace("https://community.funcraft.net/forums/", "")
        key = key.replace("/", "").split(".")[0]
        NEW_VALID_URLS[key] = []
        maxx = grab_page_count(url)
        for i in range(1, maxx + 1):
            suffix = "" if i == 1 else f"page-{i}"
            NEW_VALID_URLS[key].append(url + suffix)
    return NEW_VALID_URLS

